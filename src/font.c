Image *ParseCharacter(byte *character, pixel color) {
    int width = character[0];
    int height = character[1];

    Image *image = CreateImage(width, height);

    int i;
    for (i = 0; i < width * height; i++) {  
        if (character[i + 2] == '1') {
            image->pixels = character[i + 2];
        } else {
            image->pixels = TRANSPARENT;
        }
    }
}

